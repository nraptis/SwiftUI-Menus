    private static func build(name: String,
                              widthPad: Int, heightPad: Int,
                              widthPhoneLandscape: Int, heightPhoneLandscape: Int,
                              widthPhonePortrait: Int, heightPhonePortrait: Int) -> MainTabIconPack {
        
        let slicePadDark = AnyTextIcon(fileName: name + "_pad_0l_dark",
                                       device: .pad,
                                       orientation: .portrait,
                                       width: widthPad, height: heightPad)
        let slicePadLight = AnyTextIcon(fileName: name + "_pad_0l_light",
                                        device: .pad,
                                        orientation: .portrait,
                                        width: widthPad, height: heightPad)
        let slicePhoneLandscapeDark = AnyTextIcon(fileName:  name + "_phone_ls_0l_dark",
                                                  device: .phone,
                                                  orientation: .landscape,
                                                  width: widthPhoneLandscape, height: heightPhoneLandscape)
        let slicePhoneLandscapeLight = AnyTextIcon(fileName:  name + "_phone_ls_0l_light",
                                                   device: .phone,
                                                   orientation: .landscape,
                                                   width: widthPhoneLandscape, height: heightPhoneLandscape)
        let slicePhonePortraitDark = AnyTextIcon(fileName:  name + "_phone_po_0l_dark",
                                                 device: .phone,
                                                 orientation: .portrait,
                                                 width: widthPhonePortrait, height: heightPhonePortrait)
        let slicePhonePortraitLight = AnyTextIcon(fileName:  name + "_phone_po_0l_light",
                                                  device: .phone,
                                                  orientation: .portrait,
                                                  width: widthPhonePortrait, height: heightPhonePortrait)
        let slicePadDarkDisabled = AnyTextIcon(fileName: name + "_pad_0l_dark_disabled",
                                       device: .pad,
                                       orientation: .portrait,
                                       width: widthPad, height: heightPad)
        let slicePadLightDisabled = AnyTextIcon(fileName: name + "_pad_0l_light_disabled",
                                        device: .pad,
                                        orientation: .portrait,
                                        width: widthPad, height: heightPad)
        let slicePhoneLandscapeDarkDisabled = AnyTextIcon(fileName:  name + "_phone_ls_0l_dark_disabled",
                                                  device: .phone,
                                                  orientation: .landscape,
                                                  width: widthPhoneLandscape, height: heightPhoneLandscape)
        let slicePhoneLandscapeLightDisabled = AnyTextIcon(fileName:  name + "_phone_ls_0l_light_disabled",
                                                   device: .phone,
                                                   orientation: .landscape,
                                                   width: widthPhoneLandscape, height: heightPhoneLandscape)
        let slicePhonePortraitDarkDisabled = AnyTextIcon(fileName:  name + "_phone_po_0l_dark_disabled",
                                                 device: .phone,
                                                 orientation: .portrait,
                                                 width: widthPhonePortrait, height: heightPhonePortrait)
        let slicePhonePortraitLightDisabled = AnyTextIcon(fileName:  name + "_phone_po_0l_light_disabled",
                                                  device: .phone,
                                                  orientation: .portrait,
                                                  width: widthPhonePortrait, height: heightPhonePortrait)
        
        return MainTabIconPack(slicePadDark: slicePadDark,
                               slicePadLight: slicePadLight,
                               slicePhoneLandscapeDark: slicePhoneLandscapeDark,
                               slicePhoneLandscapeLight: slicePhoneLandscapeLight,
                               slicePhonePortraitDark: slicePhonePortraitDark,
                               slicePhonePortraitLight: slicePhonePortraitLight,
                               slicePadDarkDisabled: slicePadDarkDisabled,
                               slicePadLightDisabled: slicePadLightDisabled,
                               slicePhoneLandscapeDarkDisabled: slicePhoneLandscapeDarkDisabled,
                               slicePhoneLandscapeLightDisabled: slicePhoneLandscapeLightDisabled,
                               slicePhonePortraitDarkDisabled: slicePhonePortraitDarkDisabled,
                               slicePhonePortraitLightDisabled: slicePhonePortraitLightDisabled)
        
    }
